package com.platzi.javatests.util;

public class CalculatorUtil {
    public static int getAddition(int numberOne, int numberTwo){
        return numberOne + numberTwo;
    }

    public static int getSubtraction(int numberOne, int numberTwo){
        return numberOne - numberTwo;
    }

    public static int getDivision(int numberOne, int numberTwo){
        return numberOne / numberTwo;
    }

    public static int getMultiplication(int numberOne, int numberTwo){
        return numberOne * numberTwo;
    }
}
